#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	int		j;
	int		num;

	j = 1;
	i = 0;
	num = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			j *= -1;
		i++;
	}
	while (str[i] <= '9' && str[i] >= '0')
	{
		num = num * 10 + str[i] - 48;
		i++;
	}
	return (j * num);
}
