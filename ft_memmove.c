#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t n)
{
	if (!dst && !src)
		return (NULL);
	if (dst <= src)
		return (ft_memcpy(dst, src, n));
	else
		while (n--)
			((char *)dst)[n] = ((char *)src)[n];
	return (dst);
}