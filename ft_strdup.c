#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*des;

	len = ft_strlen(s) + 1;
	des = malloc(sizeof(char) * len);
	if (!des)
		return (NULL);
	return (ft_memmove(des, s, len));
}
